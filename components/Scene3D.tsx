'use client';

import { useState, useEffect } from 'react';

export function Scene3D() {
  const [isClient, setIsClient] = useState(false);

  useEffect(() => {
    setIsClient(true);
  }, []);

  // Only render on client side
  if (!isClient || typeof window === 'undefined') {
    return null;
  }

  // Simple fallback - just render a subtle gradient background
  // This ensures the page works even if Three.js fails
  return (
    <div className="fixed inset-0 z-0 pointer-events-none opacity-10">
      <div className="absolute inset-0 bg-gradient-to-br from-purple/20 via-violet/10 to-lightPurple/20">
        {/* Animated gradient orbs as fallback */}
        <div className="absolute top-1/4 left-1/4 w-96 h-96 bg-purple/30 rounded-full blur-3xl animate-pulse"></div>
        <div className="absolute bottom-1/4 right-1/4 w-96 h-96 bg-violet/30 rounded-full blur-3xl animate-pulse" style={{ animationDelay: '1s' }}></div>
      </div>
    </div>
  );
}
